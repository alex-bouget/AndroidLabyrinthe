package com.cppfdm.labyrinthe.game;

public class Coord {
    private int x;
    private int y;

    /*
    Generate a coordinate
    @param x: x element of the Coordinate
    @param y: y element of the coordinate
    @return : A Coord object
     */
    public Coord(int x, int y) {
        this.x = x;
        this.y = y;
    }
}
